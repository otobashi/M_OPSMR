INSERT INTO dbo.M_OPSMR_TB_SUB_PROD_MAPPING
SELECT A.SUBSIDIARY
      ,CASE COPY_T.COL 
	        WHEN 1 THEN 'SALE'
	        WHEN 2 THEN 'COI'
	        WHEN 3 THEN 'COST'
	        WHEN 4 THEN 'RATE'
	  END AS KPI_TYPE
	  ,A.PRODUCT
	  ,A.ORD
	  ,B.DISPLAY_NAME
FROM   M_PRODUCT_TYPE(NOLOCK) A
       LEFT JOIN
	   M_OPSMR_TB_OP_RATE_SUB_MST(NOLOCK) B
	   ON A.SUBSIDIARY = B.MAPPING_CODE
	  ,(SELECT 1 AS COL UNION ALL
	    SELECT 2 AS COL UNION ALL
		SELECT 3 AS COL UNION ALL
		SELECT 4 AS COL ) COPY_T
WHERE  A.TYPE = 'COST'
;